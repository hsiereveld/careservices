export function getTranslation(key: string, language: string = 'es'): string {
  const langTranslations = serviceTemplateTranslations[language as keyof typeof serviceTemplateTranslations] || serviceTemplateTranslations.es;
  return langTranslations[key as keyof typeof langTranslations] || key;
}

export function localizeServiceTemplate(template: any, language: string = 'es') {
  return {
    ...template,
    name: getTranslation(template.nameKey, language),
    description: getTranslation(template.descriptionKey, language),
  };
}

